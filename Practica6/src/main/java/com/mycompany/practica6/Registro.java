    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // Todo menos subtotal, iva, total a pagar
        // Es decir: Cantidad de dias, mensualidad, destino turistico, tipo habitacion y tipo pago
        
        String sTipoHabitacion = "";
        
        if(rbtnSuite.isSelected()){ sTipoHabitacion = "Suite";}
        else if(rbtnDoble.isSelected()){ sTipoHabitacion = "Doble";}
        else if(rbtnSencilla.isSelected()){ sTipoHabitacion = "Sencilla";}
        
        cantDias = (short) sbCantidadDias.getValue();
        
        switch(listDestinoTuristico.getSelectedIndex()){
            case 0: destinoTuristico = 500;
            case 1: destinoTuristico = 600;
            case 2: destinoTuristico = 700;
            case 3: destinoTuristico = 800;
            case 4: destinoTuristico = 900;
        }
        
        if(rbtnSuite.isSelected()){ tipoHabitacion = 500;}
        else if(rbtnDoble.isSelected()){ tipoHabitacion = 400;}
        else if(rbtnSencilla.isSelected()){ tipoHabitacion = 300;}
        
        
        list.agregarCotizacion(cantDias, mensualidad, destinoTuristico, tipoHabitacion,listDestinoTuristico.getSelectedValue(), sTipoHabitacion, String.valueOf(cbTipoPago.getSelectedItem()));
        
        
    }//GEN-LAST:event_btnRegistrarActionPerformed
 
